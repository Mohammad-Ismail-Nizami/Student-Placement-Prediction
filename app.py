PROJECT_ROOT = Path(__file__).resolve().parent
MODEL_PATH = PROJECT_ROOT / "models" / "placement_prediction_model.pkl"


# ---------------------------------------------------------
# Load trained model
# ---------------------------------------------------------
@st.cache_resource
def load_model():
    """
    Load and cache the trained placement prediction model.
    """
    if not MODEL_PATH.exists():
        raise FileNotFoundError(
            f"Model file was not found at: {MODEL_PATH}"
        )

    return joblib.load(MODEL_PATH)


try:
    model = load_model()
except Exception as error:
    st.error("The placement prediction model could not be loaded.")
    st.exception(error)
    st.stop()


